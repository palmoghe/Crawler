/**
 * Class used to extract and store 
 * author's affiliation text and email
 * @author Pallavi
 *
 */
class Affil {
	private String aff;
	private String email;

	public String getAff() {
		return aff;
	}

	public void setAff(String aff) {
		this.aff = aff;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}